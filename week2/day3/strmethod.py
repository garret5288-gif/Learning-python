user_name = input("Enter a username: ").strip().lower()# Remove leading/trailing whitespace and convert to lowercase
print("Hello, " + user_name + "!")

user_name2 = input("Enter your username: ").strip().title()# Remove leading/trailing whitespace and convert to title case
print("Hello, " + user_name2 + "!")

user_name3 = input("Enter your username: ").strip().upper()# Remove leading/trailing whitespace and convert to uppercase
print("Hello, " + user_name3 + "!")
